    private void btnBuscarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProdutoActionPerformed
       
        //abre a tela de busca
        BuscaProduto busca = new BuscaProduto(this, true);
        
        busca.setListaProdutos(produtoController.findAllAtivos()); //entrega os produtos
        busca.setVisible(true); 
        
        //Quando a janela fechar, verifica se ele escolheu algo
        Produto selecionado = busca.getProdutoSelecionado();
        
        if (selecionado != null){
            //pergunta a quantidade (padrão vai ser 1)
            String qtdStr = JOptionPane.showInputDialog(this, "Quantidade:", "1");
            if(qtdStr != null & !qtdStr.trim().isEmpty()){
                try{
                    int qtd = Integer.parseInt(qtdStr);
                    adicionarProdutoAoCarrinho(selecionado, qtd);
                } catch (NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "Quantidade inválida!");
                }
            }
        }
    }//GEN-LAST:event_btnBuscarProdutoActionPerformed
    
    private void adicionarProdutoAoCarrinho(Produto produto, int quantidade) {
        // Cria a linha do pedido
        ItemVenda novoItem = new ItemVenda();
        novoItem.setProduto(produto);
        novoItem.setQuantidade(quantidade);
        novoItem.setPrecoUnitario(produto.getPrecoUnidade());
        novoItem.setSubtotal(produto.getPrecoUnidade() * quantidade);
        novoItem.setVenda(vendaAtual); // Conecta o item à venda atual

        // Adiciona na lista
        vendaAtual.getItens().add(novoItem);

        // Adiciona na Tabela visual
        DefaultTableModel modelo = (DefaultTableModel) tblCarrinho.getModel();
        modelo.addRow(new Object[]{
            produto.getId(),
            produto.getNome(),
            quantidade,
            String.format("R$ %.2f", produto.getPrecoUnidade()),
            String.format("R$ %.2f", novoItem.getSubtotal())
        });

        atualizarTotalVisor();
    }

    private void atualizarTotalVisor() {
        double total = 0.0;
        for (ItemVenda item : vendaAtual.getItens()) {
            total += item.getSubtotal();
        }
        // lblTotal é aquele JLabel gigante na sua direita
        lblValorTotal.setText(String.format("R$ %.2f", total)); 
    }
    
    public void editarQuantidadeItem(int linha) {
        ItemVenda item = vendaAtual.getItens().get(linha);
        String nomeProduto = item.getProduto().getNome();
        
        String input = JOptionPane.showInputDialog(this, 
                "Nova quantidade para " + nomeProduto + ":", item.getQuantidade());
                
        if (input != null && !input.trim().isEmpty()) {
            try {
                int novaQtd = Integer.parseInt(input);
                if (novaQtd > 0) {
                    // Atualiza o objeto no back-end
                    item.setQuantidade(novaQtd);
                    item.setSubtotal(item.getPrecoUnitario() * novaQtd);
                    
                    // Atualiza os valores visuais na Tabela
                    DefaultTableModel modelo = (DefaultTableModel) tblCarrinho.getModel();
                    modelo.setValueAt(novaQtd, linha, 1); // Atualiza a coluna 1 (Qtd)
                    modelo.setValueAt(String.format("R$ %.2f", item.getSubtotal()), linha, 3); // Atualiza a coluna 3 (Subtotal)
                    
                    // Recalcula o valor total da venda
                    atualizarTotalVisor();
                } else {
                    JOptionPane.showMessageDialog(this, "A quantidade deve ser maior que zero!");
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Quantidade inválida!");
            }
        }
    }
    
    public void removerItemCarrinho(int linha) {
        ItemVenda item = vendaAtual.getItens().get(linha);
        
        int resposta = JOptionPane.showConfirmDialog(this, 
            "Deseja remover '" + item.getProduto().getNome() + "' do carrinho?", 
            "Remover Item", JOptionPane.YES_NO_OPTION);
            
        if (resposta == JOptionPane.YES_OPTION) {
            // Remove da lista em memória
            vendaAtual.getItens().remove(linha);
            
            // Remove da tabela visual
            DefaultTableModel modelo = (DefaultTableModel) tblCarrinho.getModel();
            modelo.removeRow(linha);
            
            atualizarTotalVisor();
        }
    }
