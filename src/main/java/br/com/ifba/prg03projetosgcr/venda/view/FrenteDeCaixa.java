    private void btnNomeClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNomeClienteActionPerformed
        // Abre a janela de busca de clientes
        BuscaCliente busca = new BuscaCliente(this, true);
            
        //Busca todos os clientes no banco e entrega para a tabela da janela
        busca.setListaClientes(clienteController.findAll());
        busca.setVisible(true); // O sistema pausa aqui até a janela fechar

        // Quando a janela fecha, o sistema captura quem foi selecionado
        Cliente selecionado = busca.getClienteSelecionado();
        
        // Se o usuário escolheu alguém (não cancelou a tela)
        if (selecionado != null) {
            // Salva o cliente no carrinho atual!
            vendaAtual.setCliente(selecionado);

            btnNomeCliente.setText(selecionado.getNome());
        }
    }//GEN-LAST:event_btnNomeClienteActionPerformed

    private void btnFinalizarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarVendaActionPerformed
        
        //Verifica se tem algo no carrinho
        if (vendaAtual.getItens().isEmpty()) {
            JOptionPane.showMessageDialog(this, "O carrinho está vazio!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try{
            // Lê a forma de pagamento direto na tela
            String opcaoSelecionada = cbxFormaPagamento.getSelectedItem().toString();
            FormaPagamento formaEscolhida = FormaPagamento.valueOf(opcaoSelecionada);
            
            vendaAtual.setFormaPagamento(formaEscolhida);
            
            //aciona o controller para salvar no banco
            vendaController.realizarVenda(vendaAtual);
                
            JOptionPane.showMessageDialog(this, "Venda Finalizada com Sucesso!",
                    "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
            //limpa a tela para o proximo cliente
            vendaAtual = new Venda();
            btnNomeCliente.setText("Informar Cliente");
            ((DefaultTableModel) tblCarrinho.getModel()).setNumRows(0);
            atualizarTotalVisor();
        } catch (Exception ex){
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", 
                        JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnFinalizarVendaActionPerformed

