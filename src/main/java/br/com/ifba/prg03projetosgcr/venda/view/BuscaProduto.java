    private void txtBuscaKeyReleased(java.awt.event.KeyEvent evt) {
        String textoDigitado = txtBusca.getText().toLowerCase();
        List<Produto> produtosFiltrados = new ArrayList<>();
        
        for (Produto p : listaProdutos) {
            if (p.getNome().toLowerCase().contains(textoDigitado)) {
                produtosFiltrados.add(p);
            }
        }
        atualizarTabelaBusca(produtosFiltrados);
    }
    
    private void tblProdutosMouseClicked(java.awt.event.MouseEvent evt) {
        // Verifica se foi um duplo clique (clickCount == 2)
        if (evt.getClickCount() == 2) {
            int linhaClicada = tblProdutos.getSelectedRow();
            if (linhaClicada != -1) {
                // Pega o ID da primeira coluna da tabela
                Long idSelecionado = (Long) tblProdutos.getValueAt(linhaClicada, 0);
                
                // Acha o produto na lista e guarda na variável
                for (Produto p : listaProdutos) {
                    if (p.getId().equals(idSelecionado)) {
                        this.produtoSelecionado = p;
                        break;
                    }
                }
                this.dispose(); // Fecha a janelinha de busca
            }
        }
    }
