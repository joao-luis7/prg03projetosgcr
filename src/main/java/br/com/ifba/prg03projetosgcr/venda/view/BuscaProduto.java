    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // Pega a linha que o usuário clicou na tabela
        int linhaClicada = tblProdutos.getSelectedRow();
        
        // Se ele não clicou em nenhuma linha (linha -1), o sistema avisa
        if (linhaClicada == -1) {
            JOptionPane.showMessageDialog(this, 
                "Por favor, selecione um produto na lista primeiro.", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Se clicou, faz o mesmo processo do duplo clique: pega o ID e acha o produto
        Long idSelecionado = (Long) tblProdutos.getValueAt(linhaClicada, 0);
        
        for (Produto p : listaProdutos) {
            if (p.getId().equals(idSelecionado)) {
                this.produtoSelecionado = p;
                break;
            }
        }
        
        // Fecha a janela
        this.dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

