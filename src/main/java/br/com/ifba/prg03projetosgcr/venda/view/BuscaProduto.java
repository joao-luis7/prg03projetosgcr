    public void setListaProdutos(List<Produto> produtos) {
        this.listaProdutos = produtos;
        atualizarTabelaBusca(produtos);
    }
    
    private void atualizarTabelaBusca(List<Produto> produtosParaMostrar) {
        DefaultTableModel modelo = (DefaultTableModel) tblProdutos.getModel();
        modelo.setNumRows(0); // Limpa a tabela

        for (Produto p : produtosParaMostrar) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                String.format(java.util.Locale.US, "%.2f", p.getPrecoUnidade())
            });
        }
    }
    
    
