    private void calcularPrecoIdeal() {
        try {
            // Pega os textos digitados e troca vírgula por ponto 
            String textoCusto = txtPrecoCusto.getText().trim().replace(",", ".");
            String textoMargem = txtMargemLucro.getText().trim().replace(",", ".");
            
            // Só faz a conta se os dois campos estiverem preenchidos
            if (!textoCusto.isEmpty() && !textoMargem.isEmpty()) {
                double custo = Double.parseDouble(textoCusto);
                double margem = Double.parseDouble(textoMargem);
                
                // Custo + (Custo * (Margem / 100))
                double precoIdeal = custo + (custo * (margem / 100.0));
                
                // Formata o resultado com 2 casas decimais e joga no campo de Preço da Unidade
                // Obs: Usei Locale.US para garantir que saia com ponto (ex: 45.50) e não quebre o botão salvar
                txtPrecoSugerido.setText(String.format(java.util.Locale.US, "%.2f", precoIdeal));
            }
        } catch (NumberFormatException e) {
            // Se o usuário digitar "abc" sem querer, o sistema ignora e não quebra a tela
        }
    }
    
    private void configurarCalculadoraLucro() {
        // Cria um ouvinte de teclado
        java.awt.event.KeyAdapter leituraTeclado = new java.awt.event.KeyAdapter() {
            @Override
            public void keyReleased(java.awt.event.KeyEvent evt) {
                calcularPrecoIdeal(); // Chama o cálculo sempre que uma tecla for solta
            }
        };
        
        // Pendura o ouvinte nos campos de Custo e Margem
        txtPrecoCusto.addKeyListener(leituraTeclado);
        txtMargemLucro.addKeyListener(leituraTeclado);
    }
    
