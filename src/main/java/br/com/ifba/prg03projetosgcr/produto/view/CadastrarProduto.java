    private void btnCadastrarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarProdutoActionPerformed
        // TODO add your handling code here:
        try {
            String nome = txtNomeProduto.getText().trim();
            String descricao = txtDescricao.getText().trim();
            String estoqueTexto = txtEstoqueAtual.getText().trim();
            String precoUnidadeTexto = txtPrecoUnidade.getText().trim();
            String precoCustoTexto = txtPrecoCusto.getText().trim(); 

            // Validação básica de tela
            if (nome.isEmpty() || precoUnidadeTexto.isEmpty() || estoqueTexto.isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "Preencha o Nome, Estoque e Preço da unidade!", 
                    "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Tratamento seguro de Números (Troca vírgula por ponto automaticamente)
            int estoque = Integer.parseInt(estoqueTexto);
            double precoUnidade = Double.parseDouble(precoUnidadeTexto.replace(",", "."));
            
            // O preço de custo pode ser opcional. Se estiver vazio, salva como 0.0
            double precoCusto = 0.0;
            if (!precoCustoTexto.isEmpty()) {
                precoCusto = Double.parseDouble(precoCustoTexto.replace(",", "."));
            }

            // Leitura do Status (Pega a bolinha "Ativar")
            boolean ativo = rbtnAtivar.isSelected(); 

            // Monta a entidade e manda para o banco (Persistência)
            Produto produto = new Produto();
            produto.setNome(nome);
            produto.setDescricao(descricao);
            produto.setQuantidadeEstoque(estoque);
            produto.setPrecoUnidade(precoUnidade);
            produto.setPrecoCusto(precoCusto);
            produto.setAtivo(ativo);

            produtoController.save(produto);

            JOptionPane.showMessageDialog(this, 
                "Produto adicionado ao estoque com sucesso!", 
                "Sucesso", JOptionPane.INFORMATION_MESSAGE);

            // Recarrega a tabela na tela de trás e fecha a janelinha
            if (listarProdutos != null) {
                listarProdutos.atualizarTabela();
            }
            this.dispose();

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, 
                "Por favor, digite apenas números válidos no Preço e no Estoque.", 
                "Erro de Formato", JOptionPane.ERROR_MESSAGE);
        } catch (RuntimeException ex) {
            JOptionPane.showMessageDialog(this, 
                ex.getMessage(), "Erro na Regra de Negócio", javax.swing.JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, 
                "Erro interno ao salvar o produto.", "Erro Crítico", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarProdutoActionPerformed

