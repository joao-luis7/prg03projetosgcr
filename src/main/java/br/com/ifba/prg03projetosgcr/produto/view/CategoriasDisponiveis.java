    private void atualizarTabela(List<Categoria> lista) {
        DefaultTableModel modelo = (DefaultTableModel) tblCategorias.getModel();
        modelo.setNumRows(0);
        for (Categoria c : lista) {
            modelo.addRow(new Object[]{
                c.getId(), c.getNome(), c.getDescricao()
            });
        }
    }
