    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
                // Pega a linha que o usuário clicou na tabela
        int linhaClicada = tblCategorias.getSelectedRow();
        
        // Se ele não clicou em nenhuma linha (linha -1), o sistema avisa
        if (linhaClicada == -1) {
            JOptionPane.showMessageDialog(this, 
                "Por favor, selecione uma categoria na lista primeiro.", 
                "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Se clicou, faz o mesmo processo do duplo clique: pega o ID e acha o produto
        Long idSelecionado = (Long) tblCategorias.getValueAt(linhaClicada, 0);
        
        for (Categoria c : listaCategorias) {
            if (c.getId().equals(idSelecionado)) {
                this.categoriaSelecionada = c;
                break;
            }
        }
        
        // Fecha a janela
        this.dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

